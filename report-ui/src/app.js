/* App shell + router wiring + the guided first path. */

import { h, clear } from "./lib/dom.js";
import { startRouter, onRoute, parseRoute } from "./lib/router.js";
import { RUNS, getRun } from "./lib/bundle.js";
import { verdictChip, btn, pill, panel, stateClass } from "./components/kit.js";
import { renderGallery } from "./surfaces/gallery.js";
import { renderReport } from "./surfaces/report.js";
import { renderVerifier } from "./surfaces/verifier.js";
import { renderCi } from "./surfaces/ci.js";
import { renderCompare } from "./surfaces/compare.js";

const GUIDE = [
  { route: "#/", copy: "Six real runs, produced by the CLI. Start with the pair that carries the argument." },
  { route: "#/compare", copy: "Same script, same gate. One checkpoint resumes the run; the other only looks like it does." },
  { route: "#/run/hf-model-only", copy: "Here is why it failed — optimizer, scheduler and RNG state were never persisted." },
  { route: "#/run/hf-complete/verify", copy: "Now check the proof yourself: your browser re-hashes every file, then try to break it." },
  { route: "#/run/hf-model-only/ci", copy: "And the same evidence as a pull-request check that blocks the merge." },
];

const GUIDE_KEY = "flashpilot.guide.dismissed";

const chipSlot = h("div", {});
const main = h("main", { class: "shell-main", id: "main", tabindex: "-1" });
const guideSlot = h("div", {});

function nav(route) {
  const runId = route.name === "run" ? route.runId : null;
  const links = [
    ["Gallery", "#/", route.name === "gallery"],
    ["Compare", "#/compare", route.name === "compare"],
  ];
  if (runId) {
    links.push(
      ["Report", `#/run/${runId}`, route.name === "run" && route.tab === "report"],
      ["Verify", `#/run/${runId}/verify`, route.name === "run" && route.tab === "verify"],
      ["CI", `#/run/${runId}/ci`, route.name === "run" && route.tab === "ci"],
    );
  }
  links.push(["About", "#/about", route.name === "about"]);

  return h(
    "nav",
    { class: "shell-nav", "aria-label": "surfaces" },
    ...links.map(([label, href, current]) =>
      h("a", { class: "nav-link", href, "aria-current": current ? "page" : null, text: label }),
    ),
  );
}

