function runSurface(route) {
  const run = getRun(route.runId);
  if (!run) {
    return h(
      "div",
      { class: "stack" },
      h("h1", { text: "No such run" }),
      h("p", { class: "lede", text: `There is no sample called "${route.runId}" in this sandbox.` }),
      btn("Back to the gallery", { href: "#/", primary: true }),
    );
  }
  if (route.tab === "verify") return renderVerifier(run);
  if (route.tab === "ci") return renderCi(run);
  return renderReport(run);
}

function currentRun(route) {
  if (route.name === "run") return getRun(route.runId);
  if (route.name === "compare") return getRun(route.params?.get("a") ?? "hf-model-only");
  return null;
}

function renderGuide(route) {
  if (sessionStorage.getItem(GUIDE_KEY) === "1") return null;
  const index = GUIDE.findIndex((step) => matchesGuide(step.route, route));
  const step = index === -1 ? null : GUIDE[index];
  if (!step) return null;
  const next = GUIDE[index + 1];

  return h(
    "div",
    { class: "guide" },
    h("span", { class: "guide-step", text: `${index + 1}/${GUIDE.length}` }),
    h("p", { class: "guide-copy", text: step.copy }),
    h(
      "div",
      { class: "guide-actions" },
      next ? btn("Next", { href: next.route, primary: true }) : btn("Explore freely", { href: "#/" }),
      btn("Dismiss", {
        onClick: () => {
          sessionStorage.setItem(GUIDE_KEY, "1");
          clear(guideSlot);
        },
      }),
    ),
  );
}

function matchesGuide(guideRoute, route) {
  const target = parseRoute(guideRoute);
  if (target.name !== route.name) return false;
  if (target.name !== "run") return true;
  return target.runId === route.runId && (target.tab ?? "report") === (route.tab ?? "report");
}

function render(route) {
  clear(chipSlot).append(verdictChip(currentRun(route)));

  const header = document.querySelector(".shell-header");
  const oldNav = header.querySelector(".shell-nav");
  const newNav = nav(route);
  if (oldNav) header.replaceChild(newNav, oldNav);
  else header.append(newNav);

  clear(main);
  if (route.name === "gallery") main.append(renderGallery());
  else if (route.name === "compare") main.append(renderCompare(route.params));
  else if (route.name === "about") main.append(renderAbout());
  else main.append(runSurface(route));

  clear(guideSlot);
  const guide = renderGuide(route);
  if (guide) guideSlot.append(guide);

  window.scrollTo({ top: 0, behavior: "instant" });
  document.title = titleFor(route);
}

function titleFor(route) {
  const run = currentRun(route);
  if (run) return `${run.verdict} · ${run.title} — FlashPilot`;
  return "FlashPilot — resume qualification report";
}

export function mount(root) {
  const header = h(
    "header",
    { class: "shell-header" },
    h("a", { class: "brand", href: "#/" }, "FlashPilot", h("span", { text: "RESUME QUALIFICATION" })),
    chipSlot,
  );

  const restart = btn("Restart guided tour", {
    onClick: () => {
      sessionStorage.removeItem(GUIDE_KEY);
      if (window.location.hash === "#/") render(parseRoute());
      else window.location.hash = "#/";
    },
  });

  const footer = h(
    "footer",
    { class: "shell-footer" },
    h("span", { text: "Frameworks save checkpoints. FlashPilot proves they can resume." }),
    h(
      "span",
      { class: "row" },
      restart,
      pill("static · no backend", "neutral"),
      pill("verdicts copied, never computed", "data"),
    ),
  );

  root.append(
    h("a", { class: "skip-link", href: "#main", text: "Skip to content" }),
    header,
    main,
    guideSlot,
    footer,
  );

  onRoute(render);
  startRouter();
}
